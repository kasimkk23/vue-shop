<template>
  <div class="addToCart">
    <button class="btn btn-primary" @click="addToCart">Add to Cart</button>
  </div>
</template>

<script>
export default {
  name: "AddToCart",
  props: {
    name: String,
    price: String,
    productId: String,
    image: String
  },
  data() {
    return {
      item: {
        productName: this.name,
        productPrice: this.price,
        product_id: this.productId,
        productImage: this.image
      }
    };
  },
  methods: {
    addToCart() {
      $("#miniCart").modal("show");
      this.$store.commit("addToCart", this.item);
    }
  }
};
</script>

